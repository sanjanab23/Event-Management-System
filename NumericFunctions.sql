-- ABS: Returns the absolute value of a number.
SELECT ABS(-10) AS AbsoluteValue;

-- ROUND: Rounds a number to a specified number of decimal places.
SELECT ROUND(3.14159, 2) AS RoundedNumber;

-- CEIL / CEILING: Returns the smallest integer greater than or equal to a given number.
SELECT CEIL(3.7) AS CeilNumber;

-- FLOOR: Returns the largest integer less than or equal to a given number.
SELECT FLOOR(3.7) AS FloorNumber;

-- SQRT: Returns the square root of a number.
SELECT SQRT(16) AS SquareRoot;
